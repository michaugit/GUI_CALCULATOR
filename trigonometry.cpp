#include "trigonometry.h"
#include <math.h>
#include <iostream>

using namespace std;

double sinCalc(double number){
    return sin(number);
}

double cosCalc(double number){
    return cos(number);
}

double sinhCalc(double number){
    return sinh(number);
}

double coshCalc(double number){
    return cosh(number);
}

double tanCalc(double number){
    return tan(number);
}

double tanhCalc(double number){
    return tanh(number);
}
